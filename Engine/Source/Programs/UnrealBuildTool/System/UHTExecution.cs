		static UInt128 CalculateHash(UHTModuleInfo mi)
		{
			XxHash128 xx = new();
			List<FileItem> files = new(mi.PublicUObjectClassesHeaders.Concat(mi.PublicUObjectHeaders).Concat(mi.InternalUObjectHeaders).Concat(mi.PrivateUObjectHeaders));
			files.Sort(FileItem.SortByPath);
			foreach (FileItem file in files)
			{
				xx.Append(MemoryMarshal.AsBytes(file.FullName.AsSpan()));
			}
			byte[] hash16 = xx.GetHashAndReset();
			ulong lo = BinaryPrimitives.ReadUInt64LittleEndian(hash16.AsSpan()[..8]);
			ulong hi = BinaryPrimitives.ReadUInt64LittleEndian(hash16.AsSpan()[8..]);
			return ((UInt128)hi << 64) | lo;
		}

		class TimestampsFile
		{
			internal TimestampsFile(FileReference location)
			{
				_location = location;
			}

			const uint CurrentVersion = 2;

			internal void Load(ILogger logger)
			{
				try
				{
					if (FileReference.Exists(_location))
					{
						using FileStream stream = FileReference.Open(_location, FileMode.Open);
						using BinaryReader reader = new(stream);
						uint version = reader.ReadUInt32();
						if (version != CurrentVersion)
						{
							logger.LogInformation("UHT timestamps file version mismatch");
							return;
						}

						int moduleCount = reader.ReadInt32();
						for (int i = 0; i < moduleCount; i++)
						{
							string moduleName = reader.ReadString();
							ulong hashA = reader.ReadUInt64();
							UInt128 hashB = reader.ReadUInt64();
							UInt128 hash = (hashB << 64) + hashA;
							long lastUpdateUtc = reader.ReadInt64();
							_modules.Add(moduleName, (hash, lastUpdateUtc));
						}
					}
				}
				catch
				{
				}
			}

			internal bool GetModuleInfo(string module, out UInt128 hash, out long lastUpdateUtc)
			{
				if (_modules.TryGetValue(module, out (UInt128 hash, long lastUpdateUtc) entry))
				{
					hash = entry.hash;
					lastUpdateUtc = entry.lastUpdateUtc;
					return true;
				}
				hash = 0;
				lastUpdateUtc = 0;
				return false;
			}

			internal void Update(List<UHTModuleInfo> modules)
			{
				DateTime now = DateTime.UtcNow;
				foreach (UHTModuleInfo module in modules)
				{
					_modules[module.ModuleName] = (CalculateHash(module), now.Ticks);
				}
			}

			internal void Save()
			{
				using FileStream stream = FileReference.Open(_location, FileMode.Create, FileAccess.Write);
				using BinaryWriter writer = new(stream);
				writer.Write(CurrentVersion);
				writer.Write(_modules.Count);
				foreach (KeyValuePair<string, (UInt128 hash, long lastUpdateUtc)> kv in _modules)
				{
					writer.Write(kv.Key);
					writer.Write((ulong)kv.Value.hash);
					writer.Write((ulong)(kv.Value.hash >> 64));
					writer.Write(kv.Value.lastUpdateUtc);
				}
			}

			readonly FileReference _location;
			readonly SortedDictionary<string, (UInt128 hash, long lastUpdateUtc)> _modules = [];
		}

		/// <summary>
		/// Builds and runs the header tool and touches the header directories.
		/// Performs any early outs if headers need no changes, given the UObject modules, tool path, game name, and configuration
		/// </summary>
		public static (Task run, Task timestamp) ExecuteHeaderToolIfNecessary(BuildConfiguration BuildConfiguration, FileReference? ProjectFile, TargetMakefile Makefile, string TargetName, ILogger Logger)
		{
			bool runTimestamp = false;

			TimestampsFile timestampsFile = new(FileReference.Combine(Makefile.ProjectIntermediateDirectoryNoArch.ParentDirectory!, TargetName + ".uhttimestamps"));

			Task runTask = new Task(() =>
			{
				if (ProgressWriter.bWriteMarkup)
				{
					Logger.LogInformation("@progress push 5%");
				}
				using (ProgressWriter Progress = new("Generating code...", false, Logger))
				{
					using ITimelineEvent uhtManifestScope = Timeline.ScopeEvent("UHTManifest");

					string RootLocalPath = Unreal.RootDirectory.FullName;

					// Get the path to the assemblies we care about
					string UhtAssemblyPath = typeof(UhtSession).Assembly.Location;

					// Get a composite date/time for critical assemblies being used
					DateTime CompositeTimestamp = UhtUtilities.GetLatestAssemblyDateTime(Makefile.EnabledUhtPlugins?.Select(x => x.FullName));

					bool bUHTNeedsToRun = false;

					// Check explicit settings passed to UHT - this info is also saved in the manifest but we load just the settings here
					FileReference UHTSettingsFileName = GetUHTSettingsFileName(Makefile, TargetName);
					if (!FileReference.Exists(UHTSettingsFileName))
					{
						bUHTNeedsToRun = true;
					}
					else
					{
						using (BinaryArchiveReader reader = new(UHTSettingsFileName))
						{
							// Note that even if default/omitted values cause the settings to compare equal here, the UHT module version check below 
							// will ensure that we rerun UHT when new properties are added to UHTTargetSettings
							try
							{
								UHTTargetSettings settings = new(reader);
								if (settings != Makefile.UHTTargetSettings)
								{
									bUHTNeedsToRun = true;
								}
							}
							catch (Exception)
							{
								bUHTNeedsToRun = true;
							}
						}
					}

					// ensure the headers are up to date
					if (BuildConfiguration.bForceHeaderGeneration)
					{
						bUHTNeedsToRun = true;
					}
					else if (!bUHTNeedsToRun && AreGeneratedCodeFilesOutOfDate(timestampsFile, Makefile.UObjectModules, CompositeTimestamp, Logger))
					{
						bUHTNeedsToRun = true;
					}

					// Check we're not using a different version of UHT
					FileReference ModuleInfoFileName = GetUHTModuleInfoFileName(Makefile, TargetName);
					FileReference ToolInfoFile = ModuleInfoFileName.ChangeExtension(".uhtpath");
					if (!bUHTNeedsToRun)
					{
						if (!FileReference.Exists(ToolInfoFile))
						{
							bUHTNeedsToRun = true;
						}
						else if (FileReference.ReadAllText(ToolInfoFile) != UhtAssemblyPath)
						{
							bUHTNeedsToRun = true;
						}
					}

					// Get the file containing dependencies for the generated code
					FileReference ExternalDependenciesFile = GetUHTDepsFileName(ModuleInfoFileName);
					if (AreExternalDependenciesOutOfDate(ExternalDependenciesFile))
					{
						bUHTNeedsToRun = true;
					}

					if (bUHTNeedsToRun)
					{
						Progress.Write(1, 3);

						WriteUHTManifest(Makefile, TargetName, ModuleInfoFileName, ExternalDependenciesFile);
						string ActualTargetName = String.IsNullOrEmpty(TargetName) ? "UE5" : TargetName;
						Logger.LogDebug("Parsing headers for {ActualTargetName}", ActualTargetName);

						// Generate the command line
						List<string> CmdArgs = [
							(ProjectFile != null) ? ProjectFile.FullName : TargetName,
							ModuleInfoFileName.FullName,
							"-WarningsAsErrors",
						];

						if (Unreal.IsEngineInstalled())
						{
							CmdArgs.Add("-installed");
						}

						if (BuildConfiguration.bFailIfGeneratedCodeChanges)
						{
							CmdArgs.Add("-FailIfGeneratedCodeChanges");
						}

						if (Makefile.UHTAdditionalArguments != null)
						{
							foreach (string Arg in Makefile.UHTAdditionalArguments)
							{
								if (Arg[0] == '"' && Arg[^1] == '"')
								{
									CmdArgs.Add(Arg[1..^1]);
								}
								else
								{
									CmdArgs.Add(Arg);
								}
							}
						}
						CommandLineArguments Arguments = new([.. CmdArgs]);

						StringBuilder CmdLine = new();
						foreach (string Arg in CmdArgs)
						{
							CmdLine.AppendArgument(Arg);
						}

						uhtManifestScope.Finish();

						Logger.LogDebug("  Running Internal UnrealHeaderTool {CmdLine}", CmdLine);

						using ITimelineEvent uhtExecute = Timeline.ScopeEvent("UHT");

						// Run UHT
						IScope Timer = GlobalTracer.Instance.BuildSpan("Executing UnrealHeaderTool").StartActive();
						UnrealHeaderToolMode UHTTool = new();

						CompilationResult UHTResult = (CompilationResult)UHTTool.ExecuteAsync(Arguments, Logger).Result;
						Timer.Span.Finish();

						uhtExecute.Dispose();

						if (UHTResult != CompilationResult.Succeeded)
						{
							// On Linux and Mac, the shell will return 128+signal number exit codes if UHT gets a signal (e.g. crashes or is interrupted)
							if ((BuildHostPlatform.Current.Platform == UnrealTargetPlatform.Linux ||
								BuildHostPlatform.Current.Platform == UnrealTargetPlatform.Mac) &&
								(int)(UHTResult) >= 128
								)
							{
								// SIGINT is 2, so 128 + SIGINT is 130
								UHTResult = ((int)(UHTResult) == 130) ? CompilationResult.Canceled : CompilationResult.CrashOrAssert;
							}

							if (BuildHostPlatform.Current.Platform == UnrealTargetPlatform.Win64 &&
								(int)(UHTResult) < 0)
							{
								Logger.LogError("UnrealHeaderTool failed with exit code 0x{Result:X} - check that Unreal Engine prerequisites are installed.", (int)UHTResult);
							}

							throw new CompilationResultException(UHTResult);
						}

						// Update the tool info file
						DirectoryReference.CreateDirectory(ToolInfoFile.Directory);
						FileReference.WriteAllText(ToolInfoFile, UhtAssemblyPath);

						// Update saved UHT settings
						using (BinaryArchiveWriter writer = new(UHTSettingsFileName))
						{
							Makefile.UHTTargetSettings.Write(writer);
						}
					}
					else
					{
						uhtManifestScope.Finish();

						Logger.LogDebug("Generated code is up to date.");
					}

					Progress.Write(2, 3);

					runTimestamp = true;

					Progress.Write(3, 3);
				}
				if (ProgressWriter.bWriteMarkup)
				{
					Logger.LogInformation("@progress pop");
				}
			});
			Task timestampTask = runTask.ContinueWith((prev) =>
			{
				if (runTimestamp)
				{
					using (GlobalTracer.Instance.BuildSpan("UHTExecution.UpdateTimestamps()").StartActive())
					{
						using ITimelineEvent uhtExecute = Timeline.ScopeEvent("UHTTimestamps");
						timestampsFile.Update(Makefile.UObjectModules);
						timestampsFile.Save();
					}
				}
			}, TaskScheduler.Current);

			runTask.Start();

			return (runTask, timestampTask);
		}

		/// <summary>
		/// Will wait for execution task to finish and the reset cached directories
		/// </summary>
		/// <param name="executionTask"></param>
		/// <param name="Makefile"></param>
		public static void WaitForExecution(Task executionTask, TargetMakefile Makefile)
		{
			using ITimelineEvent uhtManifestScope = Timeline.ScopeEvent("UHTWait");
			executionTask.Wait();
			ResetCachedHeaderInfo(Makefile.UObjectModules);
		}

		static void ResetCachedHeaderInfo(List<UHTModuleInfo> UObjectModules)
		{
			foreach (UHTModuleInfo ModuleInfo in UObjectModules)
			{
				ModuleInfo.GeneratedCodeDirectory.ResetCachedInfo();
			}
		}
	}
