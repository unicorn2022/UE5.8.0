// Backing for Allocator<T>::allocate in stub builds. UnorderedMap allocates
// node and bucket storage through this. Simple bump arena: carve from a
// shared 16 MB mmap'd region, grow with another mmap if exhausted. Never
// deallocates — fine because the detour runs for one process lifetime.
struct StubArena
{
    u8* base = 0;
    u64 used = 0;
    u64 size = 0;
};
static StubArena g_stubArena;

void* StubAllocatorAllocate(u64 bytes, u64 alignment)
{
    if (!g_stubArena.base)
    {
        constexpr u64 kArenaBytes = 16ull * 1024 * 1024;
        void* m = mmap(nullptr, kArenaBytes, PROT_READ | PROT_WRITE,
                       MAP_PRIVATE | MAP_ANONYMOUS, -1, 0);
        if (m == MAP_FAILED)
        {
            const char msg[] = "[uba_stub] StubArena mmap failed\n";
            stub_write(2, msg, sizeof(msg) - 1);
            raw_syscall1(231, 1);
        }
        g_stubArena.base = (u8*)m;
        g_stubArena.size = kArenaBytes;
    }
    u64 mask = alignment - 1;
    u64 pos = (g_stubArena.used + mask) & ~mask;
    if (pos + bytes > g_stubArena.size)
    {
        const char msg[] = "[uba_stub] StubArena exhausted\n";
        stub_write(2, msg, sizeof(msg) - 1);
        raw_syscall1(231, 1);
    }
    void* p = g_stubArena.base + pos;
    g_stubArena.used = pos + bytes;
    return p;
}
