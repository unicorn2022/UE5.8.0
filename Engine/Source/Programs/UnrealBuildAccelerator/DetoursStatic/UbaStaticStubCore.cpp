{
    STUB_NOT_REACHED("aligned_alloc");
}
extern "C" void free(void*)
{
    STUB_NOT_REACHED("free");
}

// Freestanding libc-like wrappers around Linux syscalls. Required because
// we build with -nostdlib and shared UBA core code (UbaMemory.cpp,
// UbaParkingLot.cpp) calls these by their POSIX names. Using raw SYSCALL
// means we also don't rely on libc's TLS-based errno — callers inside
// UBA core use return values, not errno, for our purposes.
extern "C" void* mmap(void* addr, size_t len, int prot, int flags, int fd, long off)
{
    // raw mmap returns -errno in -4096..-1 for errors, a page-aligned
    // positive address on success. POSIX callers expect MAP_FAILED (-1)
    // on error, so collapse the error range.
    s64 r = raw_syscall6(SYS_mmap, (s64)addr, (s64)len, (s64)prot, (s64)flags, (s64)fd, (s64)off);
    if (r < 0 && r >= -4096)
        return MAP_FAILED;
    return (void*)r;
}
extern "C" int munmap(void* addr, size_t len)
{
    return (int)raw_syscall6(11 /* SYS_munmap */, (s64)addr, (s64)len, 0, 0, 0, 0);
}
extern "C" int close(int fd)
{
    return (int)raw_syscall1(SYS_close, fd);
}
// Stubs for libc-provided helpers that shared UBA core references. Most of
// these are either on dead paths (GetHugePageCount's fopen/fscanf) or used
// on the uncontended fast path that we want to work without libc. Where a
// function would actually fire, we implement it with raw syscalls so the
// behavior matches libc's semantics.
extern "C" long syscall(long num, ...)
{
    // UBA core's only callers pass up to 4 args (SYS_futex wake/wait path)
    // plus clock_gettime (2 args). Forward via va_list.
    va_list ap; va_start(ap, num);
    s64 a1 = va_arg(ap, s64);
    s64 a2 = va_arg(ap, s64);
    s64 a3 = va_arg(ap, s64);
    s64 a4 = va_arg(ap, s64);
    s64 a5 = va_arg(ap, s64);
    s64 a6 = va_arg(ap, s64);
    va_end(ap);
    return (long)raw_syscall6(num, a1, a2, a3, a4, a5, a6);
}
extern "C" int clock_gettime(int clk_id, struct timespec* tp)
{
    return (int)raw_syscall3(228 /* SYS_clock_gettime */, (s64)clk_id, (s64)tp, 0);
}
// When the stub blob is injected into a glibc-static target (qemu-system-* etc.)
// the target's GLIBC has its own per-thread `__errno_location` returning a TLS
// int.  Our glibc handlers (UbaGlibcHandlers.cpp) need to set that SAME int so
// the target's user-code reads the errno we wrote.  But the stub blob can't
// directly call into the target's libc — symbol resolution happened at the
// stub's link time, and the target's `__errno_location` address is unknown
// then.
//
// Solution: a writable function-pointer slot the patcher fills in.  The
// patcher locates the slot via an entry in the GlibcHandlerTable (kind =
// ImportSlot, name = "__errno_location") whose `offset` Build.sh stamps to
// the blob-relative VA of g_glibc_errno_hook_fn.  At injection time the
// patcher looks up the target's `__errno_location` in its `.symtab` and
// writes the address into the slot.  Our stub's `__errno_location` then
// defers to the target's per-thread errno when set, or falls back to a
// shared int (used by stub-only callers in pre-init paths or when injecting
// into a Go-static target that has no glibc errno).
//
// Not const so the slot is in .data (the linker script folds .data into
// the extracted .text blob).  The patcher updates it at injection time.
extern "C" int* (*g_glibc_errno_hook_fn)(void) __attribute__((used)) = nullptr;

extern "C" int* __errno_location()
{
    if (auto fn = __atomic_load_n(&g_glibc_errno_hook_fn, __ATOMIC_ACQUIRE))
        return fn();
    static int errval = 0;
    return &errval;
}
// __tls_get_addr — per-thread TLS for the tiny .tbss the stub really uses
// (ParkingLot::parkingLotExecutingFunction, ThreadLocalData + its guard,
// getCurrentThreadId::threadId + its guard — total 0x25 bytes per thread).
//
// Why we need a real implementation instead of a shared scratch buffer:
//   getCurrentThreadId() caches its tid in TLS. With shared scratch, the
//   first thread's tid leaks to every other thread, and CriticalSection /
//   ParkingLot reentrance checks fire incorrectly. ParkingLot's slow path
//   would also corrupt across threads. The "we never reach the slow
//   path" claim only held for trivial single-threaded targets like
//   `qemu --help`; a real workload reaches it.
//
// Implementation: a tiny tid-keyed hash table with linear probing. 64
// slots × 64 bytes = 4 KB total. The .tbss is 0x25 bytes so 64 is more
// than enough; if the table ever fills we fall back to a shared buffer
// (degenerate but no worse than the old behaviour). Slot ownership is
// claimed via a single atomic CAS on `ownerTid`. After ownership the
// thread reads/writes its 64-byte block without further synchronization.
namespace
{
    constexpr int kTlsSlots   = 64;
    constexpr int kTlsBufSize = 64;   // total .tbss is 0x25; 64 leaves headroom

    struct alignas(64) StubTlsSlot
    {
        // 0 = unowned. Set once via CAS to the syscall-returned tid.
        u32  ownerTid;
        // 64-byte per-thread block; offsets handed out match .tbss layout
        // (parkingLotExecutingFunction at 0x0, threadLocalData at 0x8, ...).
        char data[kTlsBufSize];
    };

    static StubTlsSlot g_tlsSlots[kTlsSlots] = {};

    // Last-resort fallback when all slots are owned by other threads.
    // Used only if tens of threads churn through the stub — shared, so
    // hitting it is observably wrong, but better than crashing.
    static char g_tlsFallback[kTlsBufSize];
}

extern "C" void* __tls_get_addr(void* arg)
{
    struct TlsIndex { unsigned long mod; unsigned long off; };
    auto*     idx = (TlsIndex*)arg;
    const u64 off = idx ? idx->off : 0;
    if (off >= kTlsBufSize)
        return &g_tlsFallback[0]; // unexpected oversize TLS — degrade safely

    const u32 tid = (u32)raw_syscall0(186 /* SYS_gettid */);
    const u32 hash = tid % kTlsSlots;

    for (int probe = 0; probe < kTlsSlots; ++probe)
    {
        const int slot = (hash + probe) % kTlsSlots;
        u32 cur = __atomic_load_n(&g_tlsSlots[slot].ownerTid, __ATOMIC_ACQUIRE);
        if (cur == tid)
            return &g_tlsSlots[slot].data[off];
        if (cur == 0)
        {
            u32 expected = 0;
            if (__atomic_compare_exchange_n(&g_tlsSlots[slot].ownerTid,
                    &expected, tid, false,
                    __ATOMIC_ACQ_REL, __ATOMIC_ACQUIRE))
            {
                // Fresh slot — already zero-initialized via BSS, no work.
                return &g_tlsSlots[slot].data[off];
            }
            // Lost the CAS race. Re-read this slot below.
            cur = expected;
            if (cur == tid) return &g_tlsSlots[slot].data[off];
        }
        // owned by someone else — keep probing
    }

    // Table saturated. Old shared-scratch behaviour.
    return &g_tlsFallback[off];
}
