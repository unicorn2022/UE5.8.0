// futex(word, op, val, ...). Returns the raw kernel return value.
// Non-private so it works across processes sharing the event memory.
// `__attribute__((unused))` because the first consumer is Phase 2e.4b
// (SharedEvent wait/wake); kept here so -Werror=unused-function doesn't
// trip the build in the interim.
__attribute__((unused))
static inline s64 futex_wait(volatile u32* word, u32 expected)
{
    // FUTEX_WAIT sleeps only if *word == expected at the moment of the
    // syscall. Spurious wakes + the usual EAGAIN-on-race contract apply;
    // callers should loop on their own condition.
    return raw_syscall6(SYS_futex, (s64)word, FUTEX_WAIT, (s64)expected, 0, 0, 0);
}

__attribute__((unused))
static inline s64 futex_wake(volatile u32* word, int count)
{
    // FUTEX_WAKE with `count` == INT_MAX wakes every waiter; 1 wakes one.
    return raw_syscall6(SYS_futex, (s64)word, FUTEX_WAKE, (s64)count, 0, 0, 0);
}

// ------------------------------------------------------------------
// EventFutex view (matches UbaEventFutex.inl in UBA core).
//
// Layout of one SharedEvent storage slot (13 u64s = 104 bytes):
//   offset 0..7   EventHeader (byte 0 = tag; byte 1 = EventResetType)
//   offset 8      m_initialized (bool, 1 byte)
//   offset 9..11  compiler padding
//   offset 12     m_state      (atomic u32: 0=Reset, 2=Signaled, 3=Destroyed)
//   offset 16     m_autoCount  (atomic u32: auto-reset token counter)
//
// Tag values at offset 0: 1 = EventPThread, 2 = SharedEventSemaphore,
// 3 = EventFutex. For static-detoured children, UBA session creates
// tag 3 so we can speak the protocol via futex syscalls alone.
#define UBA_EVENT_TAG_FUTEX   3u
#define UBA_RESET_AUTO        0u
#define UBA_RESET_MANUAL      1u
#define UBA_EVENT_STATE_RESET     0u
#define UBA_EVENT_STATE_SIGNALED  2u
#define UBA_EVENT_STATE_DESTROYED 3u

#define UBA_EVENT_OFFSET_STATE     12
#define UBA_EVENT_OFFSET_AUTOCOUNT 16
#define UBA_EVENT_SIZE             104

// Auto-reset Set: bump m_autoCount + wake one waiter. Mirrors
// EventFutex::Set() for EventResetType_Auto.
__attribute__((unused))
static void event_wake_auto(u8* ev)
{
    volatile u32* ac = (volatile u32*)(ev + UBA_EVENT_OFFSET_AUTOCOUNT);
    __atomic_add_fetch(ac, 1, __ATOMIC_RELEASE);
    futex_wake(ac, 1);
}

// Auto-reset IsSet with optional timeout. Returns 1 on signal, 0 if
// the event was destroyed or `timeoutMs` elapsed. 0xffffffff = infinite.
// Mirrors EventFutex::WaitAuto() semantics closely enough for stub use.
__attribute__((unused))
static int event_wait_auto(u8* ev, u32 timeoutMs = 0xffffffffu)
{
    volatile u32* st = (volatile u32*)(ev + UBA_EVENT_OFFSET_STATE);
    volatile u32* ac = (volatile u32*)(ev + UBA_EVENT_OFFSET_AUTOCOUNT);
    for (;;)
    {
        if (__atomic_load_n(st, __ATOMIC_ACQUIRE) == UBA_EVENT_STATE_DESTROYED)
            return 0;
        u32 c = __atomic_load_n(ac, __ATOMIC_ACQUIRE);
        if (c != 0)
        {
            u32 expected = c;
            if (__atomic_compare_exchange_n(ac, &expected, c - 1, 0,
                __ATOMIC_ACQUIRE, __ATOMIC_RELAXED))
                return 1;
            continue;
        }
        if (timeoutMs == 0)
            return 0;
        struct timespec ts = { 0, 0 };
        const struct timespec* tsPtr = 0;
        if (timeoutMs != 0xffffffffu)
        {
            ts.tv_sec  = (long)(timeoutMs / 1000u);
            ts.tv_nsec = (long)((timeoutMs % 1000u) * 1000000u);
            tsPtr = &ts;
        }
        raw_syscall6(SYS_futex, (s64)ac, FUTEX_WAIT, 0, (s64)tsPtr, 0, 0);
        // Even on timeout we loop once to re-check the counter (the
        // server may have signalled right before the kernel returned).
        if (tsPtr && timeoutMs != 0xffffffffu)
        {
            // After one full timed wait, give up if still no signal.
            // Approximation: re-check once, then exit if still 0.
            if (__atomic_load_n(ac, __ATOMIC_ACQUIRE) == 0 &&
                __atomic_load_n(st, __ATOMIC_ACQUIRE) != UBA_EVENT_STATE_DESTROYED)
                return 0;
        }
    }
}

