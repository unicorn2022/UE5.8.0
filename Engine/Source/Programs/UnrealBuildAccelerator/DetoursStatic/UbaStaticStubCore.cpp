// --- Go-syscall hook --------------------------------------------------------
//
// Two 14-byte JMPs are written, one at Syscall6 (redirecting to our bridge)
// and one at the end of the trampoline (jumping back to Syscall6+14 after
// executing the displaced bytes). Both use the same non-register-clobbering
// pattern:
//     FF 25 00 00 00 00              jmpq *0(%rip)
//     <8-byte absolute target>
// Total 14 bytes.
//
// The bridge is hand-written in UbaStaticStub.S. Must not be
// C++-mangled: the .S references it by plain name and so does our
// hook-install code below.
extern "C" void uba_syscall_bridge(void);

static void write_jmp14(u8* dst, u64 target)
{
    dst[0]  = 0xFF;
    dst[1]  = 0x25;
    dst[2]  = 0x00;
    dst[3]  = 0x00;
    dst[4]  = 0x00;
    dst[5]  = 0x00;
    // 8-byte target, little-endian
    for (int i = 0; i < 8; ++i)
        dst[6 + i] = (u8)(target >> (i * 8));
}

