FBlueprintGraphPin UBlueprintGraphPinLibrary::FromNativePin(UEdGraphPin* Pin)
{
	if(!Pin)
	{
		return FBlueprintGraphPin{};
	}

	UK2Node* OwningNode = Cast<UK2Node>(Pin->GetOwningNode());
	if(!OwningNode)
	{
		return FBlueprintGraphPin{};
	}

	FBlueprintGraphPin ScriptPin;
	ScriptPin.Node = OwningNode;
	ScriptPin.PinDirection = Pin->Direction;
	ScriptPin.PinName = Pin->PinName;

	int32 LogicalIndex = 0;
	for(UEdGraphPin* NodePin : OwningNode->Pins)
	{
		if(NodePin->bHidden)
		{
			continue;
		}
		if(NodePin->Direction == Pin->Direction)
		{
			if(NodePin == Pin)
			{
				ScriptPin.PinIndex = LogicalIndex;
				break;
			}
			LogicalIndex++;
		}
	}

	return ScriptPin;
}

bool UBlueprintGraphPinLibrary::IsValid(const FBlueprintGraphPin& Pin)
{
	if( Pin == FBlueprintGraphPin() )
	{
		return false;
	}

	return true;
}

bool UBlueprintGraphPinLibrary::IsSameNativePin(const FBlueprintGraphPin& Pin, const FBlueprintGraphPin& Other)
{
	const UEdGraphPin* NativePin = GetNativePinSafe(Pin);
	const UEdGraphPin* OtherNativePin = GetNativePinSafe(Other);
	if(!NativePin || !OtherNativePin)
	{
		return false;
	}

	return NativePin == OtherNativePin;
}

bool UBlueprintGraphPinLibrary::SetPinValue(const FBlueprintGraphPin& Pin, const FString& Value)
{
	const UEdGraphSchema_K2* Schema = GetDefault<UEdGraphSchema_K2>();
	if(UEdGraphPin* NativePin = GetNativePinSafe(Pin))
	{
		Schema->TrySetDefaultValue(*NativePin, Value, false);
		// Reset default values
		if (!Schema->IsPinDefaultValid(NativePin, NativePin->DefaultValue, NativePin->DefaultObject, NativePin->DefaultTextValue).IsEmpty())
		{
			Schema->ResetPinToAutogeneratedDefaultValue(NativePin, false);
			UE_LOGFMT(LogBlueprint, Warning, "Failed to set default value {Value} on {PinName}", Value, NativePin->PinName);
			return false;
		}
		return true;
	}
	return false;
}

bool UBlueprintGraphPinLibrary::TryCreateConnection(const FBlueprintGraphPin& Pin, const FBlueprintGraphPin& Other)
{
	UEdGraphPin* SelfNativePin = GetNativePinSafe(Pin);
	UEdGraphPin* OtherNativePin = GetNativePinSafe(Other);
	if(!SelfNativePin || !OtherNativePin)
	{
		return false;
	}

	const UEdGraphSchema_K2* Schema = GetDefault<UEdGraphSchema_K2>();
	return Schema->TryCreateConnection(SelfNativePin, OtherNativePin);
}

bool UBlueprintGraphPinLibrary::CanCreateConnection(const FBlueprintGraphPin& Pin, const FBlueprintGraphPin& Other)
{
	const UEdGraphPin* NativePin = GetNativePinSafe(Pin);
	const UEdGraphPin* OtherNativePin = GetNativePinSafe(Other);
	if(!NativePin || !OtherNativePin)
	{
		return false;
	}

	const UEdGraphSchema_K2* Schema = GetDefault<UEdGraphSchema_K2>();
	return Schema->CanCreateConnection(NativePin, OtherNativePin).Response != CONNECT_RESPONSE_DISALLOW;
}

