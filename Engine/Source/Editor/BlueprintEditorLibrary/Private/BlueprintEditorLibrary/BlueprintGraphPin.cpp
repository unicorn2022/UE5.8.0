FString UBlueprintGraphPinLibrary::GetPinTypeAsJsonSchema(const FBlueprintGraphPin& Pin)
{
	UEdGraphPin* NativePin = GetNativePinSafe(Pin);
	if(!NativePin)
	{
		return TEXT("");
	}

	UK2Node* OwningNode = Cast<UK2Node>(NativePin->GetOwningNode());
	if(!OwningNode)
	{
		return TEXT("");
	}

	return UBlueprintEditorLibrary::PinTypeToJsonSchema(NativePin->PinType, OwningNode->GetBlueprintClassFromNode());
}

