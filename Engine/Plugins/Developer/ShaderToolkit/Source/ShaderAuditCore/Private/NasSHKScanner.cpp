// Shared accumulator for scan functions. Groups entries by Branch+CL,
// tracks unique branches, then sorts and finalizes the result.
struct FScanAccumulator
{
	FNasScanResult Result;
	TMap<FString, int32> GroupKeyToIndex;
	TSet<FString> BranchSet;

	void AddEntry(FNasSHKEntry&& Entry, const FFileStatData& StatData = FFileStatData())
	{
		BranchSet.Add(Entry.Branch);

		const FString GroupKey = FNasBuildGroup::MakeGroupKey(Entry.Branch, Entry.CL);
		const FString FormatKey = FNasBuildGroup::MakeFormatKey(Entry.TargetType, Entry.FormatName, Entry.bIsCached);

		if (int32* ExistingIdx = GroupKeyToIndex.Find(GroupKey))
		{
			FSessionFileInventory& Inv = Result.Groups[*ExistingIdx].Formats.FindOrAdd(FormatKey);
			if (StatData.FileSize > 0)
			{
				Inv.SHKTotalBytes += StatData.FileSize;
				Inv.FileStats.Add(Entry.FullPath, StatData);
			}
			Inv.SHKFiles.Add(MoveTemp(Entry.FullPath));
		}
		else
		{
			const int32 NewIdx = Result.Groups.Num();
			GroupKeyToIndex.Add(GroupKey, NewIdx);
			FNasBuildGroup& Group = Result.Groups.AddDefaulted_GetRef();
			Group.Branch = Entry.Branch;
			Group.CL = Entry.CL;

			FSessionFileInventory& Inv = Group.Formats.Add(FormatKey);
			if (StatData.FileSize > 0)
			{
				Inv.SHKTotalBytes += StatData.FileSize;
				Inv.FileStats.Add(Entry.FullPath, StatData);
			}
			Inv.SHKFiles.Add(MoveTemp(Entry.FullPath));
		}
	}

	FNasScanResult Finalize()
	{
		Result.Groups.Sort([](const FNasBuildGroup& A, const FNasBuildGroup& B) { return A.CL > B.CL; });
		Result.Branches = BranchSet.Array();
		Result.Branches.Sort();
		return MoveTemp(Result);
	}
};

