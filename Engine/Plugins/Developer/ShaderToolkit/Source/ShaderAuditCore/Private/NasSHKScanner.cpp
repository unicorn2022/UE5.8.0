bool FNasSHKEntry::Parse(const FString& Filename, FNasSHKEntry& OutEntry)
{
	// Strip .shk extension
	FString Base = FPaths::GetBaseFilename(Filename);
	if (Base.IsEmpty())
	{
		return false;
	}

	// Format: {Branch}-{Project}-{CL}-{TargetType}-{LibraryName}-{FormatName}
	// Generated by FEditorShaderStableInfo::SaveToDisk:
	//   FString::Printf("%s-%s-%d-%s-%s-%s.shk", *BranchName, ProjectName, CL, *TargetType, *LibraryName, *FormatName)
	//
	// Branch can contain internal hyphens (e.g. "++Project+Release-1.00") and version
	// numbers that look numeric ("12.345"). Parse right-to-left where the structure is
	// unambiguous: the last 3 tokens are always FormatName, LibraryName, TargetType.
	// The CL is the rightmost pure-integer token (digits only, no dots/signs).

	TArray<FString> Tokens;
	Base.ParseIntoArray(Tokens, TEXT("-"), true);

	// Minimum: Branch(1) + Project(1) + CL(1) + TargetType(1) + LibraryName(1) + FormatName(1)
	if (Tokens.Num() < 6)
	{
		return false;
	}

	const int32 LastIdx = Tokens.Num() - 1;

	// Peel off the last 3 tokens (unambiguous, never contain hyphens in practice).
	OutEntry.FormatName = Tokens[LastIdx];
	OutEntry.LibraryName = Tokens[LastIdx - 1];

	// TargetType may span multiple tokens if it contains hyphens (e.g. "Android-ASTCClient").
	// The CL is a pure integer -- scan backward from (LastIdx - 2) to find it.
	int32 CLTokenIndex = INDEX_NONE;
	for (int32 i = LastIdx - 2; i >= 2; --i)
	{
		// Pure integer check: all characters must be digits, length >= 5 (plausible CL).
		const FString& Token = Tokens[i];
		if (Token.Len() >= 5)
		{
			bool bAllDigits = true;
			for (const TCHAR Ch : Token)
			{
				if (Ch < TEXT('0') || Ch > TEXT('9'))
				{
					bAllDigits = false;
					break;
				}
			}
			if (bAllDigits)
			{
				CLTokenIndex = i;
				break;
			}
		}
	}

	if (CLTokenIndex == INDEX_NONE)
	{
		return false;
	}

	// Need at least one token before CL for Project, and one before that for Branch.
	if (CLTokenIndex < 2)
	{
		return false;
	}

	// TargetType = everything between CL and LibraryName, rejoined with "-".
	const int32 TargetTypeStart = CLTokenIndex + 1;
	const int32 TargetTypeEnd = LastIdx - 2; // inclusive
	if (TargetTypeStart > TargetTypeEnd)
	{
		return false;
	}

	if (TargetTypeStart == TargetTypeEnd)
	{
		OutEntry.TargetType = Tokens[TargetTypeStart];
	}
	else
	{
		OutEntry.TargetType = FString::Join(
			TArrayView<FString>(Tokens.GetData() + TargetTypeStart, TargetTypeEnd - TargetTypeStart + 1),
			TEXT("-"));
	}

	// Normalize platform directory name: strip cook-flavor suffix after '_'.
	// Build directories use just the platform portion (e.g. "Windows", not "Windows_BuildSpecificFeature").
	{
		int32 UnderscoreIdx;
		if (OutEntry.TargetType.FindChar(TEXT('_'), UnderscoreIdx))
		{
			OutEntry.TargetType.LeftInline(UnderscoreIdx);
		}
	}

	// Branch = everything before (CLTokenIndex - 1), rejoined with "-".
	// Project token at (CLTokenIndex - 1) is not stored (unused).
	OutEntry.Branch = FString::Join(TArrayView<FString>(Tokens.GetData(), CLTokenIndex - 1), TEXT("-"));
	OutEntry.CL = FCString::Atoi(*Tokens[CLTokenIndex]);

	if (OutEntry.CL == 0)
	{
		return false;
	}

	return true;
}

