int32 FNasBuildGroup::GetNumCachedFormats() const
{
	int32 Count = 0;
	for (const TPair<FString, FSessionFileInventory>& Pair : Formats)
	{
		if (IsFormatCached(Pair.Key))
		{
			++Count;
		}
	}
	return Count;
}

int32 FNasBuildGroup::GetNumFormats() const
{
	TSet<FString> BaseKeys;
	for (const TPair<FString, FSessionFileInventory>& Pair : Formats)
	{
		BaseKeys.Add(GetBaseFormatKey(Pair.Key));
	}
	return BaseKeys.Num();
}

TMap<FString, FString> FNasBuildGroup::ResolveBestFormats() const
{
	TMap<FString, FString> BestFormatKeys;
	for (const TPair<FString, FSessionFileInventory>& Pair : Formats)
	{
		const FString BaseKey = GetBaseFormatKey(Pair.Key);
		FString* Existing = BestFormatKeys.Find(BaseKey);
		if (!Existing || IsFormatCached(Pair.Key))
		{
			BestFormatKeys.Add(BaseKey, Pair.Key);
		}
	}
	return BestFormatKeys;
}

