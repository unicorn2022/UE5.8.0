FString FNasSHKScanner::GetNasLocationFromConfig()
{
	FString Location;
	GConfig->GetString(
		TEXT("/Script/Engine.ShaderCompilerStats"),
		TEXT("SHKFilesLocation"),
		Location,
		GGameIni);
	return Location;
}

FString FNasSHKScanner::GetDefaultCacheDir()
{
	return FPaths::Combine(FPaths::ProjectSavedDir(), TEXT("ShaderAuditCache"));
}

FString FNasSHKScanner::GetBuildRootFromConfig()
{
	const FString ConfigPath = FPaths::Combine(
		FPaths::ProjectDir(), TEXT("Plugins/ShaderAudit/Config/ShaderAudit.ini"));

	FString BuildRoot;
	if (FPaths::FileExists(ConfigPath))
	{
		FConfigFile Config;
		Config.Read(ConfigPath);

		if (const FConfigSection* Section = Config.FindSection(TEXT("NASBrowser")))
		{
			if (const FConfigValue* Val = Section->Find(FName(TEXT("BuildRoot"))))
			{
				BuildRoot = Val->GetValue();
			}
		}
	}

	return BuildRoot;
}

FString FNasSHKScanner::GetRelativeCacheSubDir(const FNasSHKEntry& Entry)
{
	return FPaths::Combine(
		FString::Printf(TEXT("%s-CL-%d"), *Entry.Branch, Entry.CL),
		Entry.TargetType,
		TEXT("Metadata"));
}

FNasScanResult FNasSHKScanner::ScanDirectory(
	const FString& Directory,
	bool bIsCached,
	TFunction<void(const FNasScanResult&)> OnProgress,
	const std::atomic<bool>* bCancelFlag)
{
	if (Directory.IsEmpty())
	{
		return FNasScanResult();
	}

	FScanAccumulator Acc;
	static constexpr int32 ProgressInterval = 5;

	// Use IterateDirectoryStat for streaming enumeration -- each file is visited as
	// it's found by the OS, instead of collecting all filenames first.
	// On NAS this means the UI can start showing results while enumeration continues.
	// Stat variant gives us file sizes at no extra cost (SMB/NFS return stat in readdir).
	IFileManager::Get().IterateDirectoryStat(*Directory, [&](const TCHAR* FilenameOrDir, const FFileStatData& StatData) -> bool
	{
		if (bCancelFlag && bCancelFlag->load(std::memory_order_relaxed))
		{
			return false; // cancelled
		}

		if (StatData.bIsDirectory)
		{
			return true; // skip subdirectories
		}

		FString Filename = FPaths::GetCleanFilename(FilenameOrDir);

		// Fast extension check
		if (!Filename.EndsWith(TEXT(".shk"), ESearchCase::IgnoreCase))
		{
			return true;
		}

		++Acc.Result.FilesScanned;

		FNasSHKEntry Entry;
		if (!FNasSHKEntry::Parse(Filename, Entry))
		{
			return true;
		}

		++Acc.Result.FilesMatched;
		Entry.FullPath = FString(FilenameOrDir);
		Entry.bIsCached = bIsCached;
		Acc.AddEntry(MoveTemp(Entry), StatData);

		// Post incremental progress
		if (OnProgress && (Acc.Result.FilesScanned % ProgressInterval) == 0)
		{
			Acc.Result.Branches = Acc.BranchSet.Array();
			OnProgress(Acc.Result);
		}

		return true; // continue iteration
	});

	return Acc.Finalize();
}

FNasScanResult FNasSHKScanner::ScanLocalCache(const FString& CacheDir)
{
	if (CacheDir.IsEmpty() || !IFileManager::Get().DirectoryExists(*CacheDir))
	{
		return FNasScanResult();
	}

	FScanAccumulator Acc;

	// Scan recursively for ShaderStableInfo-*.shk files.
	// Expected cache layout: {CacheDir}/{Branch}-CL-{CL}/{TargetType}/Metadata/PipelineCaches/ShaderStableInfo-{Lib}-{Format}.shk
	IFileManager::Get().IterateDirectoryStatRecursively(*CacheDir, [&](const TCHAR* FilenameOrDir, const FFileStatData& StatData) -> bool
	{
		if (StatData.bIsDirectory)
		{
			return true;
		}

		FString Filename = FPaths::GetCleanFilename(FilenameOrDir);
		if (!Filename.StartsWith(TEXT("ShaderStableInfo-")) || !Filename.EndsWith(TEXT(".shk"), ESearchCase::IgnoreCase))
		{
			return true;
		}

		// Parse format/lib from filename: ShaderStableInfo-{LibraryName}-{FormatName}.shk
		FString BaseName = FPaths::GetBaseFilename(Filename);
		FString Remainder = BaseName.Mid(FCString::Strlen(TEXT("ShaderStableInfo-")));

		int32 LastHyphen = INDEX_NONE;
		Remainder.FindLastChar(TEXT('-'), LastHyphen);
		if (LastHyphen == INDEX_NONE)
		{
			return true;
		}

		FString LibraryName = Remainder.Left(LastHyphen);
		FString FormatName = Remainder.Mid(LastHyphen + 1);

		// Parse Branch, CL, TargetType from the directory path.
		// Walk up from PipelineCaches: .../Metadata/PipelineCaches/ -> parent is Metadata,
		// grandparent is TargetType, great-grandparent is {Branch}-CL-{CL}
		FString FullDir = FPaths::GetPath(FString(FilenameOrDir));
		FString PipelineCachesDir = FPaths::GetCleanFilename(FullDir);
		if (PipelineCachesDir != TEXT("PipelineCaches"))
		{
			return true; // unexpected structure
		}

		FString MetadataDir = FPaths::GetPath(FullDir);
		if (FPaths::GetCleanFilename(MetadataDir) != TEXT("Metadata"))
		{
			return true;
		}

		FString TargetType = FPaths::GetCleanFilename(FPaths::GetPath(MetadataDir));
		FString BuildDirName = FPaths::GetCleanFilename(FPaths::GetPath(FPaths::GetPath(MetadataDir)));

		// Parse Branch and CL from BuildDirName: "{Branch}-CL-{CL}"
		const FString CLMarker = TEXT("-CL-");
		int32 CLPos = BuildDirName.Find(CLMarker, ESearchCase::CaseSensitive);
		if (CLPos == INDEX_NONE)
		{
			return true;
		}

		FString Branch = BuildDirName.Left(CLPos);
		FString CLStr = BuildDirName.Mid(CLPos + CLMarker.Len());
		int32 CL = FCString::Atoi(*CLStr);
		if (CL == 0 || Branch.IsEmpty())
		{
			return true;
		}

		FNasSHKEntry Entry;
		Entry.FullPath = FString(FilenameOrDir);
		Entry.Branch = Branch;
		Entry.CL = CL;
		Entry.TargetType = TargetType;
		Entry.LibraryName = LibraryName;
		Entry.FormatName = FormatName;
		Entry.bIsCached = true;
		Acc.AddEntry(MoveTemp(Entry), StatData);

		return true;
	});

	return Acc.Finalize();
}

void FNasSHKScanner::ScanAsync(
	const FString& NasDir,
	const FString& CacheDir,
	TFunction<void(FNasScanResult)> OnLocalComplete,
	TFunction<void(FNasScanResult)> OnNasProgress,
	TFunction<void(FNasScanResult)> OnNasComplete,
	TSharedPtr<std::atomic<bool>> CancelToken)
{
	// Local scan -- fast, on TaskGraph. Uses ScanLocalCache which looks for
	// ShaderStableInfo-*.shk files recursively and parses metadata from the directory structure.
	Async(EAsyncExecution::TaskGraph, [CacheDir, OnLocalComplete]()
	{
		FNasScanResult LocalResult = ScanLocalCache(CacheDir);
		Async(EAsyncExecution::TaskGraphMainThread, [Result = MoveTemp(LocalResult), OnLocalComplete]() mutable
		{
			if (OnLocalComplete)
			{
				OnLocalComplete(MoveTemp(Result));
			}
		});
	});

	// NAS scan -- potentially slow, on a dedicated thread, with prefix filter + incremental progress
	if (!NasDir.IsEmpty())
	{
		Async(EAsyncExecution::Thread, [NasDir, OnNasProgress, OnNasComplete, CancelToken]()
		{
			auto PostProgress = [OnNasProgress](const FNasScanResult& Snapshot)
			{
				if (OnNasProgress)
				{
					FNasScanResult Copy = Snapshot;
					Async(EAsyncExecution::TaskGraphMainThread, [Result = MoveTemp(Copy), OnNasProgress]() mutable
					{
						OnNasProgress(MoveTemp(Result));
					});
				}
			};

			FNasScanResult NasResult = ScanDirectory(NasDir, /*bIsCached=*/ false, PostProgress, CancelToken.Get());
			Async(EAsyncExecution::TaskGraphMainThread, [Result = MoveTemp(NasResult), OnNasComplete]() mutable
			{
				if (OnNasComplete)
				{
					OnNasComplete(MoveTemp(Result));
				}
			});
		});
	}
	else
	{
		// No NAS configured -- fire immediately with empty result
		Async(EAsyncExecution::TaskGraphMainThread, [OnNasComplete]()
		{
			if (OnNasComplete)
			{
				FNasScanResult Empty;
				Empty.ErrorMessage = TEXT("SHKFilesLocation not configured");
				OnNasComplete(MoveTemp(Empty));
			}
		});
	}
}
