void ACompositeActor::SetIsActive(bool bInActive)
{
	bIsActive = bInActive;

	// Deactivate all other composite actors first
	if (bIsActive)
	{
		if (UCompositeWorldSubsystem* Subsystem = UWorld::GetSubsystem<UCompositeWorldSubsystem>(GetWorld()))
		{
			for (const TWeakObjectPtr<ACompositeActor> WeakActor : Subsystem->GetActors())
			{
				ACompositeActor* Actor = WeakActor.Get();
				if (Actor && Actor != this)
				{
					Actor->bIsActive = false;
					Actor->PropagateStateChange(ECompositeStateChangeType::Deactivate);
				}
			}
		}
	}

	PropagateStateChange(IsRendering() ? ECompositeStateChangeType::Activate : ECompositeStateChangeType::Deactivate);

	// Update the view projection matrix sent to the material parameter collection when changing which actor is active.
	if (bIsActive && IsValid(ViewProjectionComponent))
	{
		ViewProjectionComponent->ForceUpdate();
	}
}

