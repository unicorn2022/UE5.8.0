UCameraComponent* ACompositeActor::GetCameraComponent() const
{
	if (CameraActor.IsValid())
	{
		return CameraActor->GetComponentByClass<UCameraComponent>();
	}

	return nullptr;
}

void ACompositeActor::SetCameraComponent(UCameraComponent* InComponent)
{
	SetCameraActor(InComponent ? InComponent->GetOwner() : nullptr);
}

const TSoftObjectPtr<AActor>& ACompositeActor::GetCameraActor() const
{
	return CameraActor;
}

void ACompositeActor::SetCameraActor(const TSoftObjectPtr<AActor>& InActor)
{
	// Skip redundant work when sequencer evaluation calls this many times per frame.
	if (CameraActor.ToSoftObjectPath() == InActor.ToSoftObjectPath())
	{
		return;
	}

	CameraActor = InActor;

	if (IsValid(ViewProjectionComponent))
	{
		ViewProjectionComponent->SetCameraActor(InActor);
	}

	if (AActor* Actor = CameraActor.Get())
	{
		DetectAndStoreSpawnableBinding(Actor);
	}
	// On a null InActor we preserve the binding so Tick can re-resolve through sequencer despawns
	// and transient null-pushes from track-section boundaries in Multi-User. User-driven clears
	// (reset-to-default, picker -> None) flow through PostEditChangeChainProperty / the
	// customization helper, which clear the binding explicitly. BP/C++ callers that intend a full
	// clear should use ClearCameraActor() instead.
}

void ACompositeActor::ClearCameraActor()
{
#if WITH_EDITOR
	Modify();
#endif
	CameraActor.Reset();

	if (IsValid(ViewProjectionComponent))
	{
		ViewProjectionComponent->SetCameraActor(nullptr);
	}

	DetectAndStoreSpawnableBinding(nullptr);
}

