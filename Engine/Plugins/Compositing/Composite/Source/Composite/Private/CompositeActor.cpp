template<>
UCompositeSceneCapture2DComponent* ACompositeActor::FindOrCreateSceneCapture(const UCompositePassBase* InPass, int32 InIndex, FName InBaseName)
{
	if (!ensureMsgf(InPass, TEXT("Expected non-null composite pass.")))
	{
		return nullptr;
	}

	if (!ensure(InIndex >= 0))
	{
		return nullptr;
	}

	if (!SceneCapturesPerPass.Contains(InPass))
	{
		SceneCapturesPerPass.Add(InPass, FSceneCaptureComponentArray{});
	}

	TArray<TObjectPtr<UCompositeSceneCapture2DComponent>>& SceneCaptures = SceneCapturesPerPass[InPass].Components;

	if (SceneCaptures.IsValidIndex(InIndex) && IsValid(SceneCaptures[InIndex]))
	{
		return SceneCaptures[InIndex];
	}

	const FName UniqueName = MakeUniqueObjectName(this, UCompositeSceneCapture2DComponent::StaticClass(), InBaseName);
	UCompositeSceneCapture2DComponent* Component = NewObject<UCompositeSceneCapture2DComponent>(this, UniqueName, RF_Transactional);
	Component->SetupAttachment(RootComponent);
	Component->RegisterComponent();
	Component->ComponentTags.Add(UE::Composite::DefaultCompositeTag);
	AddInstanceComponent(Component);
	
	const int32 MinSize = InIndex + 1;
	if (SceneCaptures.Num() < MinSize)
	{
		SceneCaptures.SetNumZeroed(MinSize);
	}
	SceneCaptures[InIndex] = Component;

#if WITH_EDITOR
	FModuleManager::LoadModuleChecked<FLevelEditorModule>("LevelEditor").BroadcastComponentsEdited();
#endif

	return Component;
}

template<>
UCompositeShadowReflectionCatcherComponent* ACompositeActor::FindOrCreateSceneCapture(const UCompositePassBase* InPass, int32 InIndex, FName InBaseName)
{
	if (!ensureMsgf(InPass, TEXT("Expected non-null composite pass.")))
	{
		return nullptr;
	}

	if (!ensure(InIndex >= 0))
	{
		return nullptr;
	}

	if (!SceneCapturesPerPass.Contains(InPass))
	{
		SceneCapturesPerPass.Add(InPass, {});
	}

	TArray<TObjectPtr<UCompositeSceneCapture2DComponent>>& SceneCaptureArray = SceneCapturesPerPass[InPass].Components;

	if (SceneCaptureArray.IsValidIndex(InIndex) && IsValid(SceneCaptureArray[InIndex]))
	{
		return Cast<UCompositeShadowReflectionCatcherComponent>(SceneCaptureArray[InIndex]);
	}

	const FName UniqueName = MakeUniqueObjectName(this, UCompositeShadowReflectionCatcherComponent::StaticClass(), InBaseName);
	UCompositeShadowReflectionCatcherComponent* Component = NewObject<UCompositeShadowReflectionCatcherComponent>(this, UniqueName, RF_Transactional);
	Component->SetupAttachment(RootComponent);
	Component->RegisterComponent();
	Component->ComponentTags.Add(UE::Composite::DefaultCompositeTag);
	AddInstanceComponent(Component);

	const int32 MinSize = InIndex + 1;
	if (SceneCaptureArray.Num() < MinSize)
	{
		SceneCaptureArray.SetNumZeroed(MinSize);
	}
	SceneCaptureArray[InIndex] = Component;

#if WITH_EDITOR
	FModuleManager::LoadModuleChecked<FLevelEditorModule>("LevelEditor").BroadcastComponentsEdited();
#endif

	return Component;
}

void ACompositeActor::DestroySceneCaptures(const UCompositePassBase* InPass)
{
	if (!ensureMsgf(InPass, TEXT("Expected non-null composite pass.")))
	{
		return;
	}

	bool bComponentRemoved = false;

	FSceneCaptureComponentArray RemovedArray;
	if (SceneCapturesPerPass.RemoveAndCopyValue(InPass, RemovedArray))
	{
		for (TObjectPtr<UCompositeSceneCapture2DComponent>& RemovedComponent : RemovedArray.Components)
		{
			if (IsValid(RemovedComponent))
			{
				RemoveInstanceComponent(RemovedComponent);
				RemovedComponent->UnregisterComponent();
				RemovedComponent->DestroyComponent();

				bComponentRemoved = true;
			}
		}
	}

#if WITH_EDITOR
	if (bComponentRemoved)
	{
		if (FLevelEditorModule* LevelEditorModule = FModuleManager::GetModulePtr<FLevelEditorModule>("LevelEditor"))
		{
			LevelEditorModule->BroadcastComponentsEdited();
		}
	}
#endif
}

bool ACompositeActor::IsRendering() const
{
	// Hidden in game
	const UWorld* World = GetWorld();
	if (IsValid(World) && World->IsGameWorld() && IsHidden())
	{
		return false;
	}

	// Hidden in editor
#if WITH_EDITOR
	if (IsTemporarilyHiddenInEditor())
	{
		return false;
	}
#endif

	return GetIsEnabled() && GetIsActive();
}

bool ACompositeActor::IsInVPRole() const
{
	// We are in a valid camera role if the user has not assigned a role or the current VPSettings role matches the
	// assigned role.
	UVirtualProductionRolesSubsystem* VPRolesSubsystem = GEngine->GetEngineSubsystem<UVirtualProductionRolesSubsystem>();

	return !VPRole.IsValid() || (VPRolesSubsystem && VPRolesSubsystem->HasActiveRole(VPRole));
}

void ACompositeActor::PropagateStateChange(ECompositeStateChangeType ChangeType)
{
	for (TObjectPtr<UCompositeLayerBase>& Layer : CompositeLayers)
	{
		if (IsValid(Layer))
		{
			Layer->OnRenderingStateChange(ChangeType);
		}
	}

	// Remove state case
	if (ChangeType != ECompositeStateChangeType::Activate)
	{
		if (UCompositeWorldSubsystem* Subsystem = UWorld::GetSubsystem<UCompositeWorldSubsystem>(GetWorld()))
		{
			Subsystem->ResetRenderWork();
			Subsystem->ResetBuiltInRenderPassOptions();
		}
	}
}


