	bool success = true;

	for (SpecificBodyCases BodyCases : AllTestCases)
	{
		// Reset error statistics (tracked in arcseconds)
		double AverageErrorArcsec = 0.0;
		double MaxErrorArcsec = 0.0;
		int SampleCount = 0;

		// Tolerance: pass if absolute error <= max(relative tol, absolute tol)
		// This handles values near 0° cleanly: the absolute floor kicks in so that
		// e.g. a DEC of -0.15° isn't subject to an impossibly tight 0.1% = 0.54" tolerance.
		const double AbsTolDeg = BodyCases.AbsoluteErrorArcsec / 3600.0;
		const double RelTolFactor = BodyCases.RelativeErrorPercent / 100.0;

		// Get Body Name
		FString BodyName = FString("Unknown");
		if (UEnum* EnumClass = StaticEnum<EVSOP87BodyType>())
		{
			BodyName = EnumClass->GetNameStringByValue(static_cast<int64>(BodyCases.VSOP87BodyType));
		}

		// Helper: check one angle (degrees), record stats, emit error on failure
		auto CheckAngle = [&](const FRADECCase& TheCase, double Computed, double Expected, const TCHAR* Label)
		{
			double AbsErrDeg = FMath::Abs(Computed - Expected);
			double ToleranceDeg = FMath::Max(AbsTolDeg, FMath::Abs(Expected) * RelTolFactor);

			double AbsErrArcsec = AbsErrDeg * 3600.0;
			double TolArcsec = ToleranceDeg * 3600.0;

			++SampleCount;
			AverageErrorArcsec += (AbsErrArcsec - AverageErrorArcsec) / SampleCount;
			MaxErrorArcsec = FMath::Max(MaxErrorArcsec, AbsErrArcsec);

			if (AbsErrDeg > ToleranceDeg)
			{
				AddError(FString::Printf(TEXT("Case failed: %s error %.4f\" (tolerance %.4f\") - Body = %s, Case = %s"),
					Label, AbsErrArcsec, TolArcsec, *BodyName, *TheCase.ToString()));
				success = false;
			}
		};

		for (FRADECCase Case : BodyCases.Cases)
		{
			// Computed results
			double RAJ2000Hours = 0.0;         // topocentric J2000/ICRF — matches Horizons "Astrometric RA & Dec" with surface observer
			double DECJ2000Degrees = 0.0;
			double RAGeocentricHours = 0.0;    // geocentric of-date — used for 3D placement
			double DECGeocentricDegrees = 0.0;
			double RAHours = 0.0;              // topocentric of-date — matches Horizons "Apparent RA & Dec"
			double DECDegrees = 0.0;
			double DistanceBodyToEarthAU = 0.0;
			double DistanceBodyToSunAU = 0.0;
			double DistanceEarthToSunAU = 0.0;

			UCelestialMaths::GetBodyCelestialCoordinatesAU(Case.JulianDate, BodyCases.VSOP87BodyType, Case.Latitude, Case.Longitude, false, false, RAJ2000Hours, DECJ2000Degrees, RAHours, DECDegrees, RAGeocentricHours, DECGeocentricDegrees, DistanceBodyToEarthAU, DistanceBodyToSunAU, DistanceEarthToSunAU);

			CheckAngle(Case, RAJ2000Hours * 15.0, Case.ExpectedRAJ2000Degrees,  TEXT("RAJ2000"));
			CheckAngle(Case, DECJ2000Degrees,     Case.ExpectedDecJ2000Degrees, TEXT("DECJ2000"));
			CheckAngle(Case, RAHours * 15.0,      Case.ExpectedRADegrees,       TEXT("RA at Date"));
			CheckAngle(Case, DECDegrees,          Case.ExpectedDecDegrees,      TEXT("DEC at Date"));
		}

		AddInfo(FString::Printf(TEXT("RA/DEC Precision Test for %s — avg %.4f\", max %.4f\" (abs tol %.1f\", rel tol %.2f%%)"),
			*BodyName, AverageErrorArcsec, MaxErrorArcsec, BodyCases.AbsoluteErrorArcsec, BodyCases.RelativeErrorPercent));
	}

	return success;
