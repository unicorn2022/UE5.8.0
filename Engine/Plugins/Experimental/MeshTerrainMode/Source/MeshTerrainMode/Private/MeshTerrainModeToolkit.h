	TSharedPtr<SWidget> ToolkitWidget;
	void UpdateActiveToolProperties();
	void InvalidateCachedDetailPanelState(UObject* ChangedObject);

	/** A utility function to register the tool palettes with the ToolkitBuilder */
	void RegisterPalettes();
	FDelegateHandle ActivePaletteChangedHandle;

	TSharedPtr<SWidget> ToolShutdownViewportOverlayWidget;
	void MakeToolShutdownOverlayWidget();

	TSharedPtr<UE::ToolWidgets::SDraggableBoxOverlay> SubmodeToolPanelOverlay;
	TSharedPtr<UE::MeshTerrain::SSubmodeToolPanel> SubmodeToolPanel;
	void MakeSubmodeToolPanelOverlayWidget();
	TSharedPtr<UE::ToolWidgets::SDraggableBoxOverlay> SubmodePaletteOverlay;
	TSharedPtr<UE::MeshTerrain::SSubmodePalette> SubmodePalette;
	void MakeSubmodePaletteOverlayWidget();
	constexpr static float SubmodePalettePadding = 4.0f;
	
	void InitializeSubmodes();
	TSharedPtr<UE::MeshTerrain::FSubmode> GetDefaultSubmode() const;
	TMap<FName, TSharedPtr<UE::MeshTerrain::FSubmode>> Submodes;

	// activates and deactivates submodes
	void ActivateSubmode(TSharedPtr<UE::MeshTerrain::FSubmode> Submode);
	void DeactivateSubmode();

	// ModelingQuickSettings widget
	void MakeQuickSettingsOverlayWidget();
	TSharedPtr<SModelingQuickSettingsWidget> QuickSettingsWidget;
	TSharedPtr<UE::ToolWidgets::SDraggableBoxOverlay> QuickSettingsOverlayWidget;
	bool bShowQuickSettingsOverlayWidget = false;

	// represents the Quick Edit (RMB context menu) customizations
	TSharedPtr<UE::MeshTerrain::FModelingQuickPropertyCustomizations> QuickEditCustomizations;

	TSharedPtr<SWidget> MakeToolShutdownButtons();

	FToolWidget_DragBoxPosition GetDetailsOverlayPosition() const;
	TOptional<FVector2f> GetDetailsOverlaySize() const;
	FToolWidget_DragBoxPosition GetQuickSettingsOverlayPosition() const;

	void MakeDetailsOverlayWidget();

	// the two different types of details views that can be built
	void MakeFullDetailsOverlayWidget();
	void MakeSectionedDetailsOverlayWidget();
	
	void UpdateDetailsOverlayWidget() const;
	void UpdateQuickSettingsOverlayWidget() const;
	TSharedPtr<UE::ToolWidgets::SDraggableBoxOverlay> DetailsOverlayWidget;
	bool bShowDetailsOverlayWidget = true;

	void MakeGizmoNumericalUIOverlayWidget();
	TSharedPtr<STransformGizmoNumericalUIOverlay> GizmoNumericalUIOverlayWidget;
	bool bShowNumericalUIOverlayWidget = true;

	/* Sectioned Details Overlay Widget */
	TSharedPtr<UE::MeshTerrain::SSectionedDetailsViewWidget> SectionedDetailsViewWidget;
	TSharedPtr<IDetailsView> SectionedDetailsView;

	TSharedRef<SWidget> MakeMenu_ModelingModeConfigSettings();

	TSharedPtr<STextBlock> ModeWarningArea;
	TSharedPtr<STextBlock> ModeHeaderArea;
	TSharedPtr<STextBlock> ToolWarningArea;
	TSharedPtr<SButton> AcceptButton;
	TSharedPtr<SButton> CancelButton;
	TSharedPtr<SButton> CompletedButton;

	FText ActiveWarning{};

	// Presets
	TSharedPtr<SWidget> ToolPresetArea;
	TSharedPtr<SWidget> MakePresetPanel();
	TSharedPtr<FAssetData> CurrentPreset;
	FSoftObjectPath CurrentPresetPath;
	FText CurrentPresetLabel;
	FToolPresetOption SelectedPresetToEdit;
	FString NewPresetLabel;
	FString NewPresetTooltip;
	FSlateIcon NewPresetIcon;

	TArray<TSharedPtr<FToolPresetOption>> AvailablePresetsForTool;
	TSharedPtr<SEditableComboBox<TSharedPtr<FString>>> PresetComboBox;
	TSharedPtr<FRecentPresetCollectionProvider> RecentPresetCollectionProvider;

	TSharedRef<SWidget> GetPresetCreateButtonContent();

	void CreateNewPresetInCollection(const FString& PresetLabel, FSoftObjectPath CollectionPath, const FString& ToolTip, FSlateIcon Icon);
	void LoadPresetFromCollection(const int32 PresetIndex, FSoftObjectPath CollectionPath);
	void UpdatePresetInCollection(const FToolPresetOption& PresetToEditIn, bool bUpdateStoredPresetValues);

	FString GetCurrentPresetPath() const { return CurrentPresetPath.GetAssetPathString(); }
	void HandlePresetAssetChanged(const FAssetData& InAssetData);
	bool HandleFilterPresetAsset(const FAssetData& InAssetData);
	
	void RebuildPresetListForTool(bool bSettingsOpened);
	TSharedRef<SWidget> MakePresetComboWidget(TSharedPtr<FString> InItem);
	bool IsPresetEnabled() const;
	void ClearPresetComboList();

