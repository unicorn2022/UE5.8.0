	bool bFirstInitializeAfterModeSetup = true;
	bool bShowActiveSelectionActions = true;

	TSharedPtr<UE::MeshTerrain::FSubmode> ActiveSubmode;
	
	TUniquePtr<UE::MeshTerrain::FStylusInputHandler> StylusInputHandler;

	// This is sort of temporary, I imagine there is a better way.
	TArray<UObject*> GetActiveToolProperties() const;
	TFunction<TArray<UObject*>(void)> GetActiveToolPropertiesImpl;

	TWeakPtr<SSplitter> LevelViewportSplitter;
	int LevelViewportSplitterSlot = INDEX_NONE;
