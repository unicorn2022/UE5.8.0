struct UEdGraphNodeBinding : ICustomBinding
{
	UE_CUSTOM_BIND_MEMBERS(UEdGraphNode, Schema, Pins);
	UEdGraphNodeBinding(TPropertySpecifier<NumMembers>& Spec);
	
	const FDualStructId SchemaId;
	const FDualStructId EdGraphPinId;

	void Save(FMemberBuilder& Dst, const UEdGraphNode& Src, const UEdGraphNode* Default, const FSaveContext& Ctx) const;
	void Load(UEdGraphNode& Dst, FStructLoadView Src, ECustomLoadMethod Method) const;
	static bool Diff(const UEdGraphNode& A, const UEdGraphNode& B, const FBindContext&) { return &A != &B; }

	static FTypedRange SavePinArray(FBindId Id, TConstArrayView<UEdGraphPin*> Pins, const FSaveContext& Ctx);
	static void LoadPinArray(UEdGraphNode& Dst, FStructRangeLoadView Src);

};

UEdGraphNodeBinding::UEdGraphNodeBinding(TCustomSpecifier<Ids, NumMembers>& Spec)
: SchemaId(IndexSchemaEdGraphNode())
, EdGraphPinId(GetStructDualId<Ids, UEdGraphPin>())
{
	Spec.Members[M::Schema] = FDeclId(SchemaId);
	Spec.Members[M::Pins] = DefaultRangeOf(FDeclId(EdGraphPinId));
}

void UEdGraphNodeBinding::Save(FMemberBuilder& Dst, const UEdGraphNode& Src, const UEdGraphNode* Default, const FSaveContext& Ctx) const
{
	// Super::Serialize(Ar);
	Dst.AddStruct(MemberIds[M::Schema], FBindId(SchemaId), SaveStruct(&Src, FBindId(SchemaId), Ctx));
	if (Src.Pins.Num())
	{
		// see UEdGraphPin::SerializeAsOwningNode()/UEdGraphPin::SerializePinArray()
		Dst.AddRange(MemberIds[M::Pins], SavePinArray(FBindId(EdGraphPinId), Src.Pins, Ctx));
	}
}

void UEdGraphNodeBinding::Load(UEdGraphNode& Dst, FStructLoadView Src, ECustomLoadMethod Method) const
{
	FMemberLoader Members(Src);
	LoadStruct(&Dst, Members.GrabStruct()); // grab schema data
	if (Members.HasMore())
	{
		LoadPinArray(Dst, Members.GrabRange().AsStructs());
	}
}

FTypedRange UEdGraphNodeBinding::SavePinArray(FBindId Id, TConstArrayView<UEdGraphPin*> Pins, const FSaveContext& Ctx)
{
	FStructRangeSaver Out(Ctx.Scratch, Pins.Num());
	for (const UEdGraphPin* Pin : Pins)
	{
		check(Pin);
		Out.AddItem(SaveStruct(Pin, Id, Ctx));
	}
	return Out.Finalize(MakeStructRangeSchema(DefaultRangeMax, Id));
}

void UEdGraphNodeBinding::LoadPinArray(UEdGraphNode& Dst, FStructRangeLoadView Src)
{
	check(Dst.Pins.IsEmpty());
	for (FStructLoadView SrcIt : Src)
	{
		UEdGraphPin* Pin = UEdGraphPin::CreatePin(&Dst);
		LoadStruct(Pin, SrcIt);
		Dst.Pins.Add(Pin);
#if PP_OVERRIDE_OBJECT_LOADING
		UEdGraphPin::ResolveReferencesToPin(Pin, /*bStrictValidation*/ true);
#endif
	}
}

