// struct id for nested ctti schema binding for UEdGraphPin (not-a-UClass)
static FDualStructId IndexSchemaEdGraphPin()
{
	FType Type =
	{
		IndexScope<FRuntimeIds, TTypename<UEdGraphPin>>(),
		FTypenameId(FRuntimeIds::IndexTypename("UEdGraphPin.Schema"))
	};
	FStructId Id = FRuntimeIds::IndexStruct(Type);
	return FDualStructId(Id);
}

// struct id for nested UE reflected schema binding for UEdGraphNode (is-a-UClass)
static FDualStructId IndexSchemaEdGraphNode()
{
	FType Type = { GetBlueprintScope(), FTypenameId(FRuntimeIds::IndexTypename("EdGraphNode.Schema")) };
	FStructId Id = FRuntimeIds::IndexStruct(Type);
	return FDualStructId(Id);
}

