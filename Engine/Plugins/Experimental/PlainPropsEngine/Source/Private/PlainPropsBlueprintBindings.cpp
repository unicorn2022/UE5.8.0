struct UK2Node_EditablePinBaseBinding : ICustomBinding
{
	UE_CUSTOM_BIND_MEMBERS(UK2Node_EditablePinBase, bIsEditable, UserDefinedPins);
	UK2Node_EditablePinBaseBinding(TCustomSpecifier<Ids, NumMembers>& Spec);

	const FDualStructId EdGraphNodeId;
	const FDualStructId UserPinInfoId;

	void Save(FMemberBuilder& Dst, const UK2Node_EditablePinBase& Src, const UK2Node_EditablePinBase* Default, const FSaveContext& Ctx) const;
	void Load(UK2Node_EditablePinBase& Dst, FStructLoadView Src, ECustomLoadMethod Method) const;
	static bool Diff(const UK2Node_EditablePinBase& A, const UK2Node_EditablePinBase& B, const FBindContext&) { return &A != &B; }
};

UK2Node_EditablePinBaseBinding::UK2Node_EditablePinBaseBinding(TCustomSpecifier<Ids, NumMembers>& Spec)
: EdGraphNodeId(GUE.DynamicIds.GetStruct(UEdGraphNode::StaticClass()))
, UserPinInfoId(GUE.DynamicIds.GetStruct(FUserPinInfo::StaticStruct()))
{
	Spec.Super = FDeclId(EdGraphNodeId);
	Spec.Members[M::bIsEditable] = Specify<bool>();
	Spec.Members[M::UserDefinedPins] = DefaultRangeOf(FDeclId(UserPinInfoId));
}

void UK2Node_EditablePinBaseBinding::Save(FMemberBuilder& Dst, const UK2Node_EditablePinBase& Src, const UK2Node_EditablePinBase* Default, const FSaveContext& Ctx) const
{
	SaveSuper(Dst, &Src, FBindId(EdGraphNodeId), Ctx);
	Dst.Add(MemberIds[M::bIsEditable], (bool)Src.bIsEditable);
	if (Src.UserDefinedPins.Num())
	{
		FStructRangeSaver Out(Ctx.Scratch, Src.UserDefinedPins.Num());
		for (const TSharedPtr<FUserPinInfo>& PinInfo : Src.UserDefinedPins)
		{
			check(PinInfo.IsValid());
			Out.AddItem(SaveStruct(PinInfo.Get(), FBindId(UserPinInfoId), Ctx));
		}
		Dst.AddRange(MemberIds[M::UserDefinedPins], Out.Finalize(MakeStructRangeSchema(DefaultRangeMax, FBindId(UserPinInfoId))));
	}
}

void UK2Node_EditablePinBaseBinding::Load(UK2Node_EditablePinBase& Dst, FStructLoadView Src, ECustomLoadMethod Method) const
{
	FMemberLoader Members(Src);
	TryLoadSuper(&Dst, Members);
	Dst.bIsEditable = Members.GrabLeaf().AsBool();
	if (Members.HasMore())
	{
		for (FStructLoadView SrcIt : Members.GrabRange().AsStructs())
		{
			TSharedPtr<FUserPinInfo> PinInfo = MakeShared<FUserPinInfo>();
			LoadStruct(PinInfo.Get(), SrcIt);
			Dst.UserDefinedPins.Add(PinInfo);
		}
	}
}

