struct UEdGraphPinBinding : ICustomBinding
{
#if WITH_EDITORONLY_DATA
	UE_CUSTOM_BIND_MEMBERS(UEdGraphPin,
		Schema, ParentPin, ReferencePassThroughConnection, LinkedTo, SubPins,
		bHidden, bNotConnectable, bDefaultValueIsReadOnly, bDefaultValueIsIgnored,
		bAdvancedView, bOrphanedPin, bHasSnappedChild, bHasSnappedParent);
#else
	UE_CUSTOM_BIND_MEMBERS(UEdGraphPin,
		Schema, ParentPin, ReferencePassThroughConnection, LinkedTo, SubPins);
#endif
	UEdGraphPinBinding(TCustomSpecifier<Ids, NumMembers>& Spec);
	
	const FDualStructId SchemaId;
	const FDualStructId PinReferenceId;

	void Save(FMemberBuilder& Dst, const UEdGraphPin& Src, const UEdGraphPin* Default, const FSaveContext& Ctx) const;
	void Load(UEdGraphPin& Dst, FStructLoadView Src, ECustomLoadMethod Method) const;
	static bool Diff(const UEdGraphPin& A, const UEdGraphPin& B, const FBindContext&) { return &A != &B; }

	static FTypedRange SavePinReferenceArray(FBindId Id, TConstArrayView<UEdGraphPin*> Pins, const FSaveContext& Ctx);
	static void LoadPinReferenceArray(UEdGraphPin& RequestingPin, TArray<UEdGraphPin*>& Pins, FStructRangeLoadView Src, EPinResolveType ResolveType);
	static void LoadPinReference(UEdGraphPin& RequestingPin, UEdGraphPin*& PinRef, FStructLoadView Struct, EPinResolveType ResolveType, int32 ArrayIdx = INDEX_NONE);

};

UEdGraphPinBinding::UEdGraphPinBinding(TCustomSpecifier<Ids, NumMembers>& Spec)
: SchemaId(IndexSchemaEdGraphPin())
, PinReferenceId(GUE.DynamicIds.GetStruct(FEdGraphPinReference::StaticStruct()))
{
	Spec.Members[M::Schema] = FDeclId(SchemaId);
	Spec.Members[M::ParentPin] = FDeclId(PinReferenceId);
	Spec.Members[M::ReferencePassThroughConnection] = FDeclId(PinReferenceId);
	Spec.Members[M::LinkedTo] = DefaultRangeOf(FDeclId(PinReferenceId));
	Spec.Members[M::SubPins] = DefaultRangeOf(FDeclId(PinReferenceId));
#if WITH_EDITORONLY_DATA
	Spec.Members[M::bHidden] = Specify<bool>();
	Spec.Members[M::bNotConnectable] = Specify<bool>();
	Spec.Members[M::bDefaultValueIsReadOnly] = Specify<bool>();
	Spec.Members[M::bDefaultValueIsIgnored] = Specify<bool>();
	Spec.Members[M::bAdvancedView] = Specify<bool>();
	Spec.Members[M::bOrphanedPin] = Specify<bool>();
	Spec.Members[M::bHasSnappedChild] = Specify<bool>();
	Spec.Members[M::bHasSnappedParent] = Specify<bool>();
#endif
}

void UEdGraphPinBinding::Save(FMemberBuilder& Dst, const UEdGraphPin& Src, const UEdGraphPin* Default, const FSaveContext& Ctx) const
{
#if PP_OVERRIDE_OBJECT_LOADING
	Dst.AddStruct(MemberIds[M::Schema], FBindId(SchemaId), SaveStruct(&Src, FBindId(SchemaId), Ctx));
	if (Src.ParentPin)
	{
		FEdGraphPinReference PinRef(Src.ParentPin);
		Dst.AddStruct(MemberIds[M::ParentPin], FBindId(PinReferenceId), SaveStruct(&PinRef, FBindId(PinReferenceId), Ctx));
	}
	if (Src.ReferencePassThroughConnection)
	{
		FEdGraphPinReference PinRef(Src.ReferencePassThroughConnection);
		Dst.AddStruct(MemberIds[M::ReferencePassThroughConnection], FBindId(PinReferenceId), SaveStruct(&PinRef, FBindId(PinReferenceId), Ctx));
	}
	if (Src.LinkedTo.Num())
	{
		// see UEdGraphPin::SerializePinArray()
		Dst.AddRange(MemberIds[M::LinkedTo], SavePinReferenceArray(FBindId(PinReferenceId), Src.LinkedTo, Ctx));
	}
	if (Src.SubPins.Num())
	{
		// see UEdGraphPin::SerializePinArray()
		Dst.AddRange(MemberIds[M::SubPins], SavePinReferenceArray(FBindId(PinReferenceId), Src.SubPins, Ctx));
	}
#if WITH_EDITORONLY_DATA
	Dst.Add(MemberIds[M::bHidden], (bool)Src.bHidden);
	Dst.Add(MemberIds[M::bNotConnectable], (bool)Src.bNotConnectable);
	Dst.Add(MemberIds[M::bDefaultValueIsReadOnly], (bool)Src.bDefaultValueIsReadOnly);
	Dst.Add(MemberIds[M::bDefaultValueIsIgnored], (bool)Src.bDefaultValueIsIgnored);
	Dst.Add(MemberIds[M::bAdvancedView], (bool)Src.bAdvancedView);
	Dst.Add(MemberIds[M::bOrphanedPin], (bool)Src.bOrphanedPin);
	Dst.Add(MemberIds[M::bHasSnappedChild], Src.HasSnappedChild());
	Dst.Add(MemberIds[M::bHasSnappedParent], Src.HasSnappedParent());
#endif
#endif
}

void UEdGraphPinBinding::Load(UEdGraphPin& Dst, FStructLoadView Src, ECustomLoadMethod Method) const
{
#if PP_OVERRIDE_OBJECT_LOADING
	FMemberLoader Members(Src);
	LoadStruct(&Dst, Members.GrabStruct()); // grab schema data
	if (Members.HasMore() && Members.PeekName() == MemberIds[M::ParentPin])
	{
		LoadPinReference(Dst, Dst.ParentPin, Members.GrabStruct(), EPinResolveType::ParentPin);
	}
	if (Members.HasMore() && Members.PeekName() == MemberIds[M::ReferencePassThroughConnection])
	{
		LoadPinReference(Dst, Dst.ReferencePassThroughConnection, Members.GrabStruct(), EPinResolveType::ReferencePassThroughConnection);
	}
	if (Members.HasMore() && Members.PeekName() == MemberIds[M::LinkedTo])
	{
		LoadPinReferenceArray(Dst, Dst.LinkedTo, Members.GrabRange().AsStructs(), EPinResolveType::LinkedTo);
	}
	if (Members.HasMore() && Members.PeekName() == MemberIds[M::SubPins])
	{
		LoadPinReferenceArray(Dst, Dst.SubPins, Members.GrabRange().AsStructs(), EPinResolveType::SubPins);
	}
#if WITH_EDITORONLY_DATA
	Dst.bHidden = Members.GrabLeaf().AsBool();
	Dst.bNotConnectable = Members.GrabLeaf().AsBool();
	Dst.bDefaultValueIsReadOnly = Members.GrabLeaf().AsBool();
	Dst.bDefaultValueIsIgnored = Members.GrabLeaf().AsBool();
	Dst.bAdvancedView = Members.GrabLeaf().AsBool();
	Dst.bOrphanedPin = Members.GrabLeaf().AsBool();
	Dst.SetHasSnappedChild(Members.GrabLeaf().AsBool());
	Dst.SetHasSnappedParent(Members.GrabLeaf().AsBool());
#endif
#endif
}

FTypedRange UEdGraphPinBinding::SavePinReferenceArray(FBindId Id, TConstArrayView<UEdGraphPin*> Pins, const FSaveContext& Ctx)
{
	FStructRangeSaver Out(Ctx.Scratch, Pins.Num());
	for (const UEdGraphPin* Pin : Pins)
	{
		FEdGraphPinReference PinRef(Pin);
		Out.AddItem(SaveStruct(&PinRef, Id, Ctx));
	}
	return Out.Finalize(MakeStructRangeSchema(DefaultRangeMax, Id));
}

void UEdGraphPinBinding::LoadPinReferenceArray(UEdGraphPin& RequestingPin, TArray<UEdGraphPin*>& Pins, FStructRangeLoadView Src, EPinResolveType ResolveType)
{
	check(Src.Num());
	check(Pins.IsEmpty());
	Pins.SetNum(Src.Num());
	int32 ArrayIdx = 0;
	for (FStructLoadView SrcIt : Src)
	{
		LoadPinReference(RequestingPin, Pins[ArrayIdx], SrcIt, ResolveType, ArrayIdx);
		++ArrayIdx;
	}
}

void UEdGraphPinBinding::LoadPinReference(UEdGraphPin& RequestingPin, UEdGraphPin*& PinRef, FStructLoadView Struct, EPinResolveType ResolveType, int32 ArrayIdx)
{
	using namespace EdGraphPin_Private;

	FEdGraphPinReference Temp;
	LoadStruct(&Temp, Struct);
	if (UEdGraphPin* Pin = Temp.Get())
	{
		PinRef = Pin;
	}
	else
	{
		checkf(!PinRef, TEXT("Interesting. The Pin already has a value when the loaded ref resolve is deferred."));
#if PP_OVERRIDE_OBJECT_LOADING
		UEdGraphPin::AddUnresolvedPin(RequestingPin, Temp.*_OwningNode, Temp.*_PinId, ResolveType, ArrayIdx);
#endif
	}
}

